#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int main(int argc, char **argv)
{
	char str[100];
	ft_putstr(ft_strcpy(str, argv[argc - 1]));

	write(1, "\n", 1);	
	return 0;	
}
